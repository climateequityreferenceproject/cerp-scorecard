            <h2>Type of Pledge</h2>
            <p>As countries have tabled their national emissions mitigation pledges with the UNFCCC, a pattern has emerged. In it, countries make both unconditional pledges &#8211; which they commit to meet, come what may &#8211; and unconditional pledges that represent their willingness to act in concert with others.</p>
        </div>
        <div id="gloss_fair">
            <h2>Fair Share</h2>
            <p>The <a href="http://unfccc.int" target="_blank">United Nations Framework Convention on Climate Change</a>, in its Article 3.1, commits its signatories to "protect the climate system for the benefit of present and future generations of humankind, on the basis of equity and in accordance with their common but differentiated responsibilities and respective capabilities." This implies that nations, and by extension their citizens, have obligations to act to protect the climate system. The <a href="http://gdrights.org" target="_blank">Greenhouse Development Rights</a> framework is an attempt to rigorously but transparently model those obligations, and this Equity Scorecards interface to the <a href="http://gdrights.org/calculator" target="_blank">GDRs calculator</a> is simply an interface that highlights the GDRs standard case, which calculates national "fair shares" of the necessary global action, and compares a country's pledge to its fair share to see how it's doing.</p>
            <p>Do note that, from any scorecard, you are able to "click through" to the underlying calculator, which allows you to modify most of the parameters in the standard case.</p>
        </div>
        <div id="gloss_dom">
            <h2>Pledged Domestic Effort</h2>
            <p>Nations can act to protect the climate system in one of two ways. They can act within their own borders, and they can act by providing international support to other countries. There are of course lots of complexities involved in qualifying and quantifying actions, but at the end of the day they have this dual aspect. Domestic effort is that effort which a nation makes within its own borders. We do not attempt to estimate levels of domestic effort, but rather take authoritative estimates of pledged effort "off the shelf" as they become available.</p>
        </div>
        <div id="gloss_intl">
            <h2>Pledged International Support</h2>
            <p>Nations can act to protect the climate system in one of two ways. They can act within their own borders, and they can act by provided international support to other countries. There are of course lots of complexities involved in qualifying and quantifying actions, but at the end of the day they have this dual aspect. International support is a kind of effort that a nation makes outside its own borders. It comes in many forms, but can typically be reported either in monetary terms or in terms of tons mitigated. We don’t attempt to estimate national levels of international support, but rather take authoritative estimates of pledged support "off the shelf" as they become available.</p>
        </div>
        <div id="gloss_gap">
            <h2>Gap</h2>
            <p>The gap shown in these Equity Scorecards represents the difference between a country's fair share of the global effort (as implied by the specified level of ambition) and its total pledge. This total pledge is the sum of its pledged domestic effort and its pledged international support. It is a national pledge gap.</p>
        </div>
