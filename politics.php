        <p class="first">The Equity Scorecard system is designed to clarify the climate equity debate by comparing national pledges against estimates of national &#8220;fair shares&#8221; in the global effort needed to meet a given global mitigation goal. Its scores are based on the <a href="http://gdrights.org/">Greenhouse Development Rights</a> (GDRs) framework, which straightforwardly models a global system of common but differentiated responsibilities and capabilities, and upon <a href="#">business-as-usual pathways</a> and <a href="http://gdrights.org/scorecard_dev/glossary.php#gloss_path">mitigation pathways</a> that are as well-vetted and &#8220;off the shelf&#8221; as possible.</p>

        <h3>Why equity, and why now?</h3>

        <p>After Durban, the equity debate is more important than ever. Moreover, as the Durban Platform negotiations evolve, this is likely to be widely recognized. Despite the continued salience of the old Annexes, the global differentiation debate is long overdue, and it will inevitably implicate <a href="http://gdrights.org/scorecard_dev/glossary.php#capacity">capacity</a> as well as <a href="http://gdrights.org/scorecard_dev/glossary.php#gloss_responsibility">responsibility</a>. There will also have to be a serious engagement with the fundamental problem of &#8220;equitable access to sustainable development.&#8221;  Without real steps towards such equitable access, we are unlikely to see the ambitious multilateral mobilizations that the climate crisis demands. Equity, as it turns out, is a precondition of short-term action as well as long-term ambition.</p>

        <p>Note that the Equity Scorecard system does not attempt to evaluate national <em>actions</em>, but only national <em>pledges</em>. It reports scores for 2020 alone, because this is the only year for which pledges exist. Thus, if a country is doing a great deal, but chooses not to express that action as a pledge, its actions will not be reflected in its score. Similarly, if a country makes a pledge that it does not actually attempt to meet &#8211; a bad faith pledge &#8211; it will be scored as if it were in good faith. Pledges, at least for the moment, are taken at face value.</p>
        
        <h2>Greenhouse Development Rights is a reference framework</h2>
        <p>The Equity Scorecards system aims to provide an easily-understood reference by which fairness of pledges can be robustly evaluated, relative to a given level of global ambition. We do not imagine that the formulas that drive our calculations of national <a href="http://gdrights.org/scorecard_dev/glossary.php#gloss_rci">Responsibility and Capacity Indexes</a> will be soon accepted as the basis of a negotiated global accord. However, we do believe that our approach is about as fair as any formula-based approach can possibly be. (For more, see for example the <a href="http://www.climatenetwork.org/publication/can-fair-effort-sharing-discussion-paper">Climate Action Network&#8217;s Fair Effort Sharing Discussion paper</a>, or BASIC&#8217;s &#8220;experts paper&#8221; on <a href="http://gdrights.org/wp-content/uploads/2011/12/EASD-final.pdf">Equitable access to sustainable development</a>.)  Also, GDRs is designed for transparency, and makes almost no limiting assumptions about institutions or mechanisms. Moreover, most all the parameterizations that define the GDRs &#8220;standard case&#8221; can be easily changed in the underlying <a href="http://gdrights.org/calculator_dev/">calculator</a> and database, which is to say that sensitivity analyses are easy to do. For these reasons and others, we believe that the scores here are quite meaningful.</p>

        <h3>When it comes to &#8220;realism,&#8221; Stringency means a great deal</h3>
        <p>It&#8217;s easy, when reviewing the allocations that GDRs and similar systems generate, to dismiss them as impractical and unrealistic. Look, for example, at the United States Scorecard; then, for further perspective, click through to the underlying <a href="http://gdrights.org/calculator_dev/">calculator</a> and examine a more detailed view of the analysis, one that projects the United States&#8217; allocation out to 2030. There are no 2030 pledges to evaluate, but if you&#8217;ve selected a stringent global mitigation pathway (e.g. the <em>AOSIS pathway</em>) you can see that the US allocation actually goes <em>negative</em> even before 2030 is reached. The US, in other words, is judged to be responsible for mitigating more tons of greenhouse gas than are emitted within its borders.</p>

        <p>Now reduce the ambition of the mitigation pathway. Take a look at the results under the far less stringent (and far more dangerous) <em>G8 pathway</em>, which reflects the stated policy of the most powerful industrialized countries. Under this pathway, the demands that GDRs makes on the US are still stringent, but not in the manner of the <em>AOSIS pathway</em>. If they still appear stringent &#8211; and to many they will &#8211; it is only because the standards of the business-as-usual world remain relevant. They appear stringent, in other words, only from the point of view of a world that is hurtling away from any chance of climatic stability.</p>

        <p>Dial up ambition to the AOSIS level, or even beyond it, to the level of the <em>Hansen-style 350 ppm pathway</em> (which is not available via the Scorecard interface, but only in the underlying Calculator) and another perspective becomes necessary, one in which you see that <em>all possible climate mitigation strategies become extremely challenging at high levels of ambition.</em> Thus, GDRs, considered at a high-level of ambition (our preference) is quite challenging. Of course it is. So is any other plausible division of global effort, however won.</p>

        <p>The assumption here is that equity is a precondition for engaged, sustained, global cooperation, and that without such cooperation the necessary stringency will remain unachievable. In this context, this Equity Scorecards system, by virtue of its grounding in the logic of sustainable development rights, can help us to a common understanding of what is really at stake in the climate reckoning.</p>
